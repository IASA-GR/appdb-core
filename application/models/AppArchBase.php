<?php
class Default_Model_AppArchBase
{
	protected $_mapper;
	protected $_appID;
	protected $_application;
	protected $_archID;
	protected $_arch;

	public function __set($name,$value)
	{
		$method = 'set'.$name;
		if (('mapper' == $name) || !method_exists($this, $method)) {
			throw new Exception("Invalid AppArch property: '$name'");
		}
		if ( is_string($value) ) {
			$value = str_replace("'","’",$value);
			$value = str_replace('"','”',$value);
		}
		$this->$method($value);
	}

	public function __get($name)
	{
		$method = 'get' . $name;
		if (('mapper' == $name) || !method_exists($this, $method)) {
			throw new Exception("Invalid AppArch property: '$name'");
		}
		$ret = $this->$method();
		if ( is_string($ret) ) {
			$ret= str_replace("'","’",$ret);
			$ret = str_replace('"','”',$ret);
		}
		return $ret;
	}

	public function setOptions(array $options)
	{
		$methods = get_class_methods($this);
		foreach ($options as $key => $value) {
			$method = 'set' . ucfirst($key);
			if (in_array($method, $methods)) {
				$this->$method($value);
			}
		}
		return $this;
	}

	public function setAppID($value)
	{
		/* if ( $value === null ) {
			$this->_appID = 'NULL';
		} else */ $this->_appID = $value;
		return $this;
	}

	public function getAppID()
	{
		return $this->_appID;
	}

	public function getApplication()
	{
		if ( $this->_application === null ) {
			$Applications = new Default_Model_Applications();
			$Applications->filter->id->equals($this->getAppID());
			if ($Applications->count() > 0) $this->_application = $Applications->items[0];
		}
		return $this->_application;
	}

	public function setApplication($value)
	{
		if ( $value === null ) {
			$this->setAppID(null);
		} else {
			$this->setAppID($value->getId());
		}
	}


	public function setArchID($value)
	{
		/* if ( $value === null ) {
			$this->_archID = 'NULL';
		} else */ $this->_archID = $value;
		return $this;
	}

	public function getArchID()
	{
		return $this->_archID;
	}

	public function getArch()
	{
		if ( $this->_arch === null ) {
			$Archs = new Default_Model_Archs();
			$Archs->filter->id->equals($this->getArchID());
			if ($Archs->count() > 0) $this->_arch = $Archs->items[0];
		}
		return $this->_arch;
	}

	public function setArch($value)
	{
		if ( $value === null ) {
			$this->setArchID(null);
		} else {
			$this->setArchID($value->getId());
		}
	}


	public function setMapper($mapper)
	{
		$this->_mapper = $mapper;
		return $this;
	}

	public function getMapper()
	{
		if (null === $this->_mapper) {
			$this->setMapper(new Default_Model_AppArchsMapper());
		}
		return $this->_mapper;
	}

	public function save()
	{
		$this->getMapper()->save($this);
	}

	public function find($id)
	{
		$this->getMapper()->find($id, $this);
		return $this;
	}

	public function fetchAll($args = null)
	{
		return $this->getMapper()->fetchAll($args);
	}

	public function toXML($recursive=false)
	{
		$XML = "<AppArch>\n";
		if ($this->_appID !== null) $XML .= "<appID>".$this->_appID."</appID>\n";
		if ( $recursive ) if ( $this->_application === null ) $this->getApplication();
		if ( ! ($this->_application === null) ) $XML .= $this->_application->toXML();
		if ($this->_archID !== null) $XML .= "<archID>".$this->_archID."</archID>\n";
		if ( $recursive ) if ( $this->_arch === null ) $this->getArch();
		if ( ! ($this->_arch === null) ) $XML .= $this->_arch->toXML();
		$XML .= "</AppArch>\n";
		return $XML;
	}
}
